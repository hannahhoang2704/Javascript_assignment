const selectSection = document.getElementById('pictures');

for (let i= 0; i<picArray.length; i++) {
  const newArticle = document.createElement('article');       //create element article
  const newP = document.createElement('p');                     //create element p

  selectSection.appendChild(newArticle);                             //append article to section
  newArticle.appendChild(newP);
  newArticle.setAttribute('id', `article-${i+1}`);    //set id for each article
  newP.appendChild(document.createTextNode(picArray[i].title));      //add heading to element p


  const newFig = document.createElement('figure');           //create element figure
  const newImg = document.createElement('img');              //create element image
  newImg.src = picArray[i].image.medium;
  newImg.alt = picArray[i].title;
  newFig.appendChild(newImg);

  const newCap = document.createElement('figcaption');       //create element figcaption
  newCap.appendChild(document.createTextNode(picArray[i].caption));
  newFig.appendChild(newCap);

  newArticle.appendChild(newFig);

  const newDescription = document.createElement('p');
  newDescription.appendChild(document.createTextNode(picArray[i].description));
  newArticle.appendChild(newDescription);                              //append description



}

function modal(evt){
  evt.preventDefault();
for (let i= 0; i<picArray.length; i++) {
  let idOfArticle = `article-${i + 1}`;
  const callArticle = document.getElementById(`${idOfArticle}`);
  const dialog = document.querySelector('dialog');            //get the modal
  const imgOfDialog = document.querySelector('img');
  const span = document.querySelector('span');
  callArticle.addEventListener('click', () => {
    dialog.showModal();
    imgOfDialog.src = picArray[i].image.large;
    imgOfDialog.alt = picArray[i].title;

  });
  span.addEventListener('click', () => {
    dialog.close();
  });
}};

const article = document.querySelector('article');
article.addEventListener('click', modal);